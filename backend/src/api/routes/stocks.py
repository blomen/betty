@router.get("/sessions/review/{date}")
def get_session_review(date: str):
    """Return the full breakdown JSON produced by session_review.py for a date."""
    path = _SESSIONS_DIR / f"{date}.json"
    if not path.exists():
        raise HTTPException(status_code=404, detail=f"no review archive for {date}")
    try:
        with path.open() as f:
            return json.load(f)
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"corrupt archive: {e}") from None


@router.get("/sessions/review/trends/rolling")
def session_trends(window: int = 7):
    """Compare the most recent <window> days vs the previous <window>.

    Returns WR / PF / net-PnL / avg-trade for both windows so the operator
    can see whether the model is drifting session-over-session.
    """
    if not _SESSIONS_DIR.exists():
        return {"recent": None, "prior": None, "delta": None}

    archives = sorted([p for p in _SESSIONS_DIR.glob("*.json") if p.stem != "all"])
    if not archives:
        return {"recent": None, "prior": None, "delta": None}

    # Most-recent first
    archives.reverse()

    def _agg(paths: list[Path]) -> dict:
        trades = wins = 0
        net = gross_win = gross_loss = 0.0
        for p in paths:
            try:
                d = json.loads(p.read_text())
            except Exception:
                continue
            o = d.get("overview", {})
            trades += int(o.get("trades", 0))
            wins += int(o.get("wins", 0))
            net += float(o.get("net_pnl", 0))
            gross_win += float(o.get("gross_win", 0))
            gross_loss += float(o.get("gross_loss", 0))
        return {
            "days_included": len(paths),
            "trades": trades,
            "wins": wins,
            "win_rate": round(wins / trades, 4) if trades else 0.0,
            "net_pnl": round(net, 2),
            "gross_win": round(gross_win, 2),
            "gross_loss": round(gross_loss, 2),
            "profit_factor": round(gross_win / gross_loss, 3) if gross_loss else None,
            "avg_trade": round(net / trades, 2) if trades else 0.0,
        }

    recent = _agg(archives[:window])
    prior = _agg(archives[window : 2 * window])

    delta = {}
    for key in ("trades", "win_rate", "net_pnl", "profit_factor", "avg_trade"):
        rv = recent.get(key)
        pv = prior.get(key)
        if rv is None or pv is None:
            delta[key] = None
        else:
            delta[key] = round(rv - pv, 4)

    return {"recent": recent, "prior": prior, "delta": delta, "window_days": window}


