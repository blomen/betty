@router.get("/_debug-obs")
def debug_obs(request: Request):
    """Diagnostic-only: decode the latest captured observation vector and
    report each segment's nonzero-count + a sample of values.

    Offsets are derived from ``SEGMENTS`` (single source of truth in
    ``rl.features.observation_index``) so we can never drift from the
    actual obs layout. The schema validator already enforces that
    ``sum(seg.size for seg in SEGMENTS) == OBSERVATION_DIM``.
    """
    import base64

    import numpy as np

    from ...db.models import StockSignal, get_session
    from ...rl.features.observation_index import SEGMENTS

    db = get_session()
    try:
        sig = (
            db.query(StockSignal)
            .filter(StockSignal.observation_b64.is_not(None))
            .order_by(StockSignal.id.desc())
            .first()
        )
        if sig is None:
            return {"error": "no signal with observation_b64"}
        raw = base64.b64decode(sig.observation_b64)
        obs = np.frombuffer(raw, dtype=np.float32)
    finally:
        db.close()

    segments_out: list[dict] = []
    cursor = 0
    for seg in SEGMENTS:
        size = seg["size"]
        labels = list(seg["labels"])
        block = obs[cursor : cursor + size]
        nz_mask = np.abs(block) > 1e-6
        values = {labels[i]: round(float(block[i]), 4) for i in range(size)}
        nonzero_values = {k: v for k, v in values.items() if abs(v) > 1e-6}
        segments_out.append(
            {
                "name": seg["name"],
                "start": cursor,
                "end": cursor + size,
                "size": size,
                "nonzero_count": int(nz_mask.sum()),
                "all_zero": bool(nz_mask.sum() == 0),
                "values": values,
                "nonzero_values": nonzero_values,
            }
        )
        cursor += size

    return {
        "signal_id": sig.id,
        "signal_ts": sig.ts.isoformat() if sig.ts else None,
        "signal_action": sig.action,
        "zone_center": sig.zone_center,
        "zone_members": sig.zone_members,
        "observation_dim_actual": int(obs.size),
        "observation_dim_db": sig.observation_dim,
        "total_nonzero_dims": int((np.abs(obs) > 1e-6).sum()),
        "total_dims": int(obs.size),
        "segments": segments_out,
    }


@router.post("/_debug-test-signal")
async def debug_test_signal(request: Request):
    """Diagnostic-only: invoke broker.on_signal with a synthetic enter_long
    signal at the current market price and return whatever the broker
    returns. Bypasses level_monitor entirely so we can isolate whether the
    broker side itself accepts entries.
    """
    rt = getattr(request.app.state, "stocks_runtime", None)
    if rt is None:
        raise HTTPException(status_code=503, detail="stocks runtime not initialized")
    adapter = rt.adapter
    if not adapter.tracker.is_flat:
        return {
            "skipped": True,
            "reason": "not flat — refusing to test on live position",
            "tracker_side": adapter.tracker.side,
        }
    # Synthesize a small enter_long signal. Try last-tick mid price; fall
    # back to a hardcoded "near-market" price so the test runs even when
    # dashboard._state["ticks"] hasn't populated yet (fresh container).
    last_price = 0.0
    try:
        from src.stocks import dashboard as _ds

        last_tick = (_ds._state.get("ticks") or [None])[-1]
        last_price = float(last_tick["price"]) if last_tick else 0.0
    except Exception:
        pass
    if last_price <= 0:
        # Fallback: query a fresh quote from TopstepX directly.
        try:
            quote = await rt.client._post(
                "/api/MarketData/getQuote",
                {"contractId": rt.client._config.contract_id},
            )
            last_price = float(quote.get("lastPrice") or quote.get("bestBid") or 0)
        except Exception:
            pass
    if last_price <= 0:
        # Last resort: hardcoded current-week NQ midpoint.
        last_price = 29450.0
    synth = {
        "action": "enter_long",
        "price": last_price,
        "stop_price": last_price - 25 * 0.25,  # 25-tick stop
        "stop_ticks": 25,
        "confidence": 0.50,  # well above 0.05 reckless floor
        "orderflow_score": 0.40,
        "zone": last_price,
        "trigger": "zone_entry",
        "cont_p": 0.55,
        "rev_p": 0.45,
        "size": 1.0,
        "reasoning": {"summary": "DEBUG TEST SIGNAL"},
    }
    result = await adapter.on_signal(synth)
    return {"signal": synth, "result": result}


@router.post("/reload-model")
async def reload_inference_model(request: Request):
    """Drop the cached LiveInference singleton so the next zone-touch
    inference re-reads dqn_v5.pt + GBT joblibs from disk.

    Called by session_cleanup.sh after a training cycle completes so the
    live broker picks up the freshly-trained weights without a container
    restart. The reload itself is lazy — the next get_dqn_inference()
    call performs torch.load + joblib loads on the inference thread, so
    this request returns immediately.
    """
    import os

    from ...rl.live_inference import reset_dqn_inference

    model_path = "/app/backend/data/rl/models/dqn_v5.pt"
    model_mtime = None
    if os.path.exists(model_path):
        model_mtime = datetime.utcfromtimestamp(os.path.getmtime(model_path)).isoformat()

    result = reset_dqn_inference()
    log.info(
        "Inference singleton reset (was %s); next zone touch will lazy-load. dqn_v5.pt mtime=%s",
        result.get("previous_instance_type"),
        model_mtime,
    )
    return {
        "reset": True,
        "previous_instance_type": result.get("previous_instance_type"),
        "dqn_v5_mtime": model_mtime,
    }


@router.post("/recover")
async def recover_trading(request: Request):
    """Self-heal a stuck-tracker halt without a container restart.

    When the reconcile loop fires `size_mismatch` because the local tracker
    believes a position is open that the broker has already closed, the
    `flatten()` path now (a) writes the missed broker_trades row from
    Trade/search history, and (b) clears the tracker. This endpoint runs
    that flow on demand and additionally clears `_halted` so trading can
    resume in the same container.
    """
    rt = getattr(request.app.state, "stocks_runtime", None)
    if rt is None:
        raise HTTPException(status_code=503, detail="stocks runtime not initialized")
    adapter = rt.adapter
    flatten_result = await adapter.flatten("manual_recover")
    cleared_halt = False
    if adapter._halted:
        adapter._halted = False
        adapter._halt_reason = ""
        cleared_halt = True
    return {
        "halt_cleared": cleared_halt,
        "tracker_flat": adapter.tracker.is_flat,
        "halted": adapter._halted,
        "halt_reason": adapter._halt_reason,
        "flatten_result": flatten_result,
    }


def _parse_ts(v: str | float | None) -> datetime | None:
    if v is None:
        return None
    if isinstance(v, (int, float)):
        return datetime.fromtimestamp(v, tz=timezone.utc).replace(tzinfo=None)
    try:
        return datetime.fromisoformat(str(v).replace("Z", "+00:00")).astimezone(timezone.utc).replace(tzinfo=None)
    except Exception:
        return None


