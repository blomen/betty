"""Stocks API routes — broker-trade ingestion + session-review access.

The local arnold app POSTs every closed round-trip here (entry/exit/PnL/signal
context). Without this, trade outcomes only existed in stdout + a 100-deep
in-memory dashboard deque, lost on container restart.

Session reviews are generated by backend/scripts/session_review.py (cron at
23:55 UTC) and archived to /app/data/rl/sessions/<YYYY-MM-DD>.json. The
endpoints here let the local frontend (and humans via curl) read them back.
"""

